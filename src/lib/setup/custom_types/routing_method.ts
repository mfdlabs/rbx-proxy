/**
 * A type that is used to represent the allowed HTTP method for the route.
 * Use ALL if you are using multiple methods.
 */
export type RoutingMethod =
  | 'get'
  | 'GET'
  | 'delete'
  | 'DELETE'
  | 'head'
  | 'HEAD'
  | 'options'
  | 'OPTIONS'
  | 'post'
  | 'POST'
  | 'put'
  | 'PUT'
  | 'patch'
  | 'PATCH'
  | 'all'
  | 'ALL';
